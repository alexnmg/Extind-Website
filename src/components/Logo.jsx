const easeInOut = (t) => (t < 0.5 ? 4 * t * t * t : 1 - Math.pow(-2 * t + 2, 3) / 2)
const clamp01 = (v) => Math.min(1, Math.max(0, v))

/** Shape path — only xR varies; both caps keep their exact curvature. */
function barPath(xR) {
  const xL = BAR_XL
  return [
    `M${xR},0`,
    `C${xR + 3.865},0 ${xR + 7.429},2.08 ${xR + 9.33},5.445`,
    `C${xR + 13.514},12.848 ${xR + 21.115},26.305 ${xR + 26.723},36.226`,
    `C${xR + 31.054},43.89 ${xR + 31.054},53.263 ${xR + 26.723},60.929`,
    `C${xR + 21.115},70.85 ${xR + 13.514},84.307 ${xR + 9.33},91.71`,
    `C${xR + 7.429},95.075 ${xR + 3.865},${Y_BOT} ${xR},${Y_BOT}`,
    `L${xL},${Y_BOT}`,
    `C${xL - 3.865},${Y_BOT} ${xL - 7.429},95.075 ${xL - 9.33},91.71`,
    `C${xL - 13.514},84.307 ${xL - 21.115},70.85 ${xL - 26.723},60.929`,
    `C${xL - 31.054},53.263 ${xL - 31.054},43.89 ${xL - 26.723},36.226`,
    `C${xL - 21.115},26.305 ${xL - 13.514},12.848 ${xL - 9.33},5.445`,
    `C${xL - 7.429},2.08 ${xL - 3.865},0 ${xL},0`,
    'Z',
  ].join(' ')
}

// The three outer transforms every letter sits inside (straight from the file)
function Chain({ children }) {
  return (
    <g transform="matrix(1,0,0,1,-11678.570484,-2454.435015)">
      <g transform="matrix(1,0,0,1,10807.022134,0)">
        <g transform="matrix(0.741053,0,0,3.896194,374.59915,-1289.85389)">{children}</g>
      </g>
    </g>
  )
}

