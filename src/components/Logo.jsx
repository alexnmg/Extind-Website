const M_LETTER = 'matrix(8.00996,0,0,1.523489,-2262.793757,456.463316)'

export default function Logo() {
  const [progress, setProgress] = useState(0)
  const [imgIdx, setImgIdx] = useState(0)
  const rafRef = useRef(0)

  useEffect(() => {
    if (window.matchMedia?.('(prefers-reduced-motion: reduce)').matches) return
    let cancelled = false

    const play = () => {
      const start = performance.now()
      const step = (now) => {
        if (cancelled) return
        const t = now - start
        if (t >= EXPAND + HOLD + COLLAPSE) {
          setProgress(0)
          return
        }
        let p
        if (t < EXPAND) p = easeInOut(t / EXPAND)
        else if (t < EXPAND + HOLD) p = 1
        else p = 1 - easeInOut((t - EXPAND - HOLD) / COLLAPSE)
        setProgress(p)
        rafRef.current = requestAnimationFrame(step)
      }
      rafRef.current = requestAnimationFrame(step)
    }

    const timer = setInterval(() => {
      setImgIdx((i) => (i + 1) % IMAGES.length)
      play()
    }, CYCLE)

    return () => {
      cancelled = true
      clearInterval(timer)
      cancelAnimationFrame(rafRef.current)
    }
  }, [])

  const gap = SHIFT * (1 - progress)
  const xR = Math.max(BAR_XL, BAR_XR_MAX - gap)
  // Shape only fits once the X has opened far enough for both caps
  const barOpacity = clamp01((progress - 0.14) / 0.14)
  const contentW = VB_W - gap

  return (
    <span className="logo" style={{ width: `${(contentW / VB_H) * LOGO_H}px` }}>
      <svg
        className="logo__svg"
        viewBox={`0 0 ${VB_W} ${VB_H}`}
        xmlns="http://www.w3.org/2000/svg"
        fill="currentColor"
        role="img"
        aria-label="Extind"
        style={{
          height: `${LOGO_H}px`,
          width: `${(VB_W / VB_H) * LOGO_H}px`,
          fillRule: 'evenodd',
          clipRule: 'evenodd',
        }}
      >
        <defs>
          <clipPath id="extind-bar-clip">
            <path d={barPath(xR)} />
          </clipPath>
        </defs>

        {/* E and the left half of the X — always fixed */}
        <Chain>
          <g transform="matrix(-8.00996,0,0,1.523489,4026.790133,456.682699)">
            <path d={X_LEFT} />
          </g>
          <g transform="matrix(8.00996,0,0,1.523489,-3186.087942,456.682699)">
            <path d={LETTER_E} />
          </g>
        </Chain>

        {/* The shape — a window onto the current image */}
        {barOpacity > 0 && (
          <g clipPath="url(#extind-bar-clip)" opacity={barOpacity}>
            <image
              href={IMAGES[imgIdx]}
              x={BAR_XL - CAP}
              y={0}
              width={BAR_XR_MAX + CAP - (BAR_XL - CAP)}
              height={VB_H}
              preserveAspectRatio="xMidYMid slice"
            />
          </g>
        )}

        {/* Right half of the X + TIND — travel with the shape */}
        <g transform={`translate(${-gap},0)`}>
          <Chain>
            <g transform={M_LETTER}>
              <path d={X_RIGHT} />
            </g>
            <g transform={M_LETTER}>
              <path d={LETTER_T} />
            </g>
            <g transform={M_LETTER}>
              <path d={LETTER_I} />
            </g>
            <g transform={M_LETTER}>
              <path d={LETTER_D} />
            </g>
            <g transform="matrix(1.688522,0,0,0.465877,-30061.85855,-282.89189)">
              <path d={LETTER_N} />
            </g>
          </Chain>
        </g>
      </svg>
    </span>
  )
}
